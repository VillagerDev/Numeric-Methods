    private void jButtonResolverSistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResolverSistActionPerformed
        if (TrabajoConMatrices.definidaPositiva(matriz)) {
            double[] solucionSist = FactorizacionDeCholesky.resolverSistemaUsandoCholesky(matriz.length, matriz, b);
            Object[][] obj1 = new Object[solucionSist.length][2];
            jTableSolucionSistema.setModel(new javax.swing.table.DefaultTableModel(obj1, new String[]{"", ""}) {
                Class[] types = new Class[]{
                    java.lang.String.class, java.lang.Double.class
                };

                @Override
                public Class getColumnClass(int columnIndex) {
                    return types[columnIndex];
                }
            });
            for (int i = 0; i < solucionSist.length; i++) {
                jTableSolucionSistema.setValueAt("x".concat(String.valueOf(i + 1)), i, 0);
                jTableSolucionSistema.setValueAt(solucionSist[i], i, 1);
            }
            jDialogResolverSistema.pack();
            jDialogResolverSistema.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(jPanel1, "La matriz incumple algunas de las condiciones para aplicar factorizacion de Cholesky", "Advertencia", JOptionPane.INFORMATION_MESSAGE);

        }

    }//GEN-LAST:event_jButtonResolverSistActionPerformed

