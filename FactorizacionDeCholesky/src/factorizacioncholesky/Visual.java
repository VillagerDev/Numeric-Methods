    private void jButtonCrearMatrizActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearMatrizActionPerformed
        try {
            int cant = Integer.parseInt(jTextFieldElementos.getText());
            if (cant > 0 && cant <= 30) {

                Object[][] obj = new Object[cant][cant];

                for (int i = 0; i < cant; i++) {
                    for (int j = 0; j < cant; j++) {
                        obj[i][j] = 0;
                    }
                }

                String[] s = new String[cant];
                Class[] c = new Class[cant];
                for (int i = 0; i < cant; i++) {
                    c[i] = java.lang.Double.class;
                }

                for (int i = 0; i < cant; i++) {
                    s[i] = "";
                }

                jTableMatriz.setModel(new javax.swing.table.DefaultTableModel(obj, s
                ) {

                    @Override
                    public Class getColumnClass(int columnIndex) {
                        return c[columnIndex];
                    }
                });
                Object[][] obj2 = new Object[cant][1];

                for (int i = 0; i < cant; i++) {
                    obj2[i][0] = 0;
                }

                String[] s2 = {""};
                jTableTerminosIndependientes.setModel(new javax.swing.table.DefaultTableModel(obj2, s2
                ) {
                    @Override
                    public Class getColumnClass(int columnIndex) {
                        return c[columnIndex];
                    }

                });

                jPanel2.setVisible(true);
                matriz = new double[cant][cant];
                b = new double[cant];

                jButtonCrearMatriz.setEnabled(false);

            } else {
                throw new NumberFormatException();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(jPanel1, "El número debe ser mayor que 0 y menor o igual que 30.", "Advertencia", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButtonCrearMatrizActionPerformed

    private void jButtonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIngresarActionPerformed
        try {
            for (int i = 0; i < matriz.length; i++) {
                for (int j = 0; j < matriz.length; j++) {
                    Object o = jTableMatriz.getValueAt(i, j);
                    matriz[i][j] = Double.parseDouble(o.toString());
                }
            }

            for (int i = 0; i < b.length; i++) {
                Object o = jTableTerminosIndependientes.getValueAt(i, 0);
                b[i] = Double.parseDouble(o.toString());
            }
            jButtonCalcFact.setVisible(true);
            jButtonResolverSist.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(jPanel1, "Ingrese valores válidos.", "Advertencia", JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_jButtonIngresarActionPerformed

    private void jButtonCalcFactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcFactActionPerformed
        if (TrabajoConMatrices.definidaPositiva(matriz)) {

            double[][] m1 = FactorizacionDeCholesky.factorizacionCholesky(matriz.length, matriz);
            double[][] m1Traspuesta = TrabajoConMatrices.traspuesta(m1);

            Object[][] obj1 = new Object[m1.length][m1.length];
            Object[][] obj2 = new Object[m1Traspuesta.length][m1Traspuesta.length];

            String[] s = new String[m1.length];
            for (int i = 0; i < s.length; i++) {
                s[i] = "";
            }

            Class[] c = new Class[m1.length];
            for (int i = 0; i < c.length; i++) {
                c[i] = java.lang.Double.class;
            }

            jTableMatrizC.setModel(new javax.swing.table.DefaultTableModel(
                    obj1,
                    s
            ) {
                Class[] types = c;

                @Override
                public Class getColumnClass(int columnIndex) {
                    return types[columnIndex];
                }

            });

            jTableMatrizCTraspuesta.setModel(new javax.swing.table.DefaultTableModel(
                    obj2,
                    s
            ) {
                Class[] types = c;

                @Override
                public Class getColumnClass(int columnIndex) {
                    return types[columnIndex];
                }
            });

            for (int i = 0; i < m1.length; i++) {
                for (int j = 0; j < m1.length; j++) {
                    jTableMatrizC.setValueAt(m1[i][j], i, j);
                }
            }

            for (int i = 0; i < m1Traspuesta.length; i++) {
                for (int j = 0; j < m1Traspuesta.length; j++) {
                    jTableMatrizCTraspuesta.setValueAt(m1Traspuesta[i][j], i, j);
                }
            }

            jDialogFactorizacion.pack();
            jDialogFactorizacion.setVisible(true);

        } else {

            JOptionPane.showMessageDialog(jPanel1, "La matriz incumple algunas de las condiciones para aplicar factorización de Cholesky", "Advertencia", JOptionPane.INFORMATION_MESSAGE);

        }


    }//GEN-LAST:event_jButtonCalcFactActionPerformed

